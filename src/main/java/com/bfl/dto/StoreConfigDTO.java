package com.bfl.dto;

public class StoreConfigDTO {
	
	private String country;
	
	private String storeName;
	
	private String storeId;
	
	private String storeShortName;
	
	private String vatRegion;
	
	private String status;
	
	private String action;
	
	private String dataName;
	
	private String costCodeTo;
	
	private String LocCodeTo;

	public String getCountry() {
		return country;
	}

	public void setCountry(String country) {
		this.country = country;
	}

	public String getStoreName() {
		return storeName;
	}

	public void setStoreName(String storeName) {
		this.storeName = storeName;
	}

	public String getStoreId() {
		return storeId;
	}

	public void setStoreId(String storeId) {
		this.storeId = storeId;
	}

	public String getStoreShortName() {
		return storeShortName;
	}

	public void setStoreShortName(String storeShortName) {
		this.storeShortName = storeShortName;
	}

	public String getVatRegion() {
		return vatRegion;
	}

	public void setVatRegion(String vatRegion) {
		this.vatRegion = vatRegion;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public String getAction() {
		return action;
	}

	public void setAction(String action) {
		this.action = action;
	}

	public String getDataName() {
		return dataName;
	}

	public void setDataName(String dataName) {
		this.dataName = dataName;
	}

	public String getCostCodeTo() {
		return costCodeTo;
	}

	public void setCostCodeTo(String costCodeTo) {
		this.costCodeTo = costCodeTo;
	}

	public String getLocCodeTo() {
		return LocCodeTo;
	}

	public void setLocCodeTo(String locCodeTo) {
		LocCodeTo = locCodeTo;
	}
	
}