public class CreditCardDTO {

	private String	ccInvoice;
	
	private String	ccname;
	
	private String	ccno;
	
	private String	ccamt;
	
	private String	cccode;
	
	private String	ccrectno;
	
	private String	ccbank;

	public String getCcInvoice() {
		return ccInvoice;
	}

	public void setCcInvoice(String ccInvoice) {
		this.ccInvoice = ccInvoice;
	}

	public String getCcname() {
		return ccname;
	}

	public void setCcname(String ccname) {
		this.ccname = ccname;
	}

	public String getCcno() {
		return ccno;
	}

	public void setCcno(String ccno) {
		this.ccno = ccno;
	}

	public String getCcamt() {
		return ccamt;
	}

	public void setCcamt(String ccamt) {
		this.ccamt = ccamt;
	}

	public String getCccode() {
		return cccode;
	}

	public void setCccode(String cccode) {
		this.cccode = cccode;
	}

	public String getCcrectno() {
		return ccrectno;
	}

	public void setCcrectno(String ccrectno) {
		this.ccrectno = ccrectno;
	}

	public String getCcbank() {
		return ccbank;
	}

	public void setCcbank(String ccbank) {
		this.ccbank = ccbank;
	}

}


